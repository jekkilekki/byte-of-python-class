# numbers + operators
print(2 + 3) # 5
print(3 - 2) # 1
print(2 * 3) # 6 #### int / float
print(3 / 2) # 1.5 부동 소수점 = float / floating point number
print(4 / 2) # 2.0 decimal = 십진수 (진수) 이 = 2 = 이진수 = binary

# Exponents / 지수
print(3 ** 2) # 9
print(3 ** 3) # 27
print(10 ** 6) # 1000000 = 10에 6제곱

# Remainders / 나머지
